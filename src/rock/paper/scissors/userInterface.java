    private void userPaperMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_userPaperMouseClicked
        
        
        switch (randomCMD().charAt(0)) {
            case 'p':
                title.setText("Draw");
                break;
            case 's':
                title.setText("You lose");
                break;
            case 'r':
                title.setText("You Win");
                break;
        }

        
    }//GEN-LAST:event_userPaperMouseClicked

