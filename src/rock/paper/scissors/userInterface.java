    public String randomCMD() {
        int Max = 2;
        int Min = 0;
        int number = Min + (int) (Math.random() * ((Max - Min) + 1));
        if (number == 0) {
            computer.setIcon(new javax.swing.ImageIcon(getClass().getResource("/rock/paper/scissors/paper.png")));
            return "paper";
        }
        if (number == 1) {
            computer.setIcon(new javax.swing.ImageIcon(getClass().getResource("/rock/paper/scissors/rock.png")));
            return "rock";
        }
        computer.setIcon(new javax.swing.ImageIcon(getClass().getResource("/rock/paper/scissors/scissors.png")));
        return "scissor";
    }

